package com.ime.music.info;
//提示词信息
public class TipInfo {
    private String hint = "";
    private int source = 0;

    public String getHint() {
        return hint;
    }

    public void setHint(String hint) {
        this.hint = hint;
    }

    public int getSource() {
        return source;
    }

    public void setSource(int source) {
        this.source = source;
    }
}
